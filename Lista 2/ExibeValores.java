public class ExibeValores {
    public static void main(String [] args) {

        byte menorValor = 19;
        short curto = 1571;
        int inteiro = 100000;
        long longo = 2147483648L;
        float flutuante = 3.14F;
        double dobro = 3.141592653589793;
        boolean booleano = true;
        char caractere = 'K';
        
        System.out.println("Byte = " + menorValor);
        System.out.println("Short = " + curto);
        System.out.println("Int = " + inteiro);
        System.out.println("Long = " + longo);
        System.out.println("Float = " + flutuante);
        System.out.println("Double = " + dobro);
        System.out.println("Boolean = " + booleano);
        System.out.println("Char = " + caractere);

    }
}
